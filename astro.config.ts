import * as fs from 'node:fs'
import cloudflare from '@astrojs/cloudflare'
import type { AstroIntegration } from 'astro'
import { defineConfig } from 'astro/config'
import compress from 'astro-compress'
import htmlMinifierNext from 'astro-html-minifier-next'
import browserslist from 'browserslist'
import { browserslistToTargets } from 'lightningcss'

const lightningCssOptions = {
  minify: true,
  errorRecovery: false,
  targets: browserslistToTargets(browserslist('defaults')),
}

const generateCloudflareRoutes: AstroIntegration = {
  name: 'generate-cloudflare-routes',
  hooks: {
    'astro:build:done': (result) => {
      const staticPages = result.pages.map((p) => `/${p.pathname}`)
      const staticAssets = []

      if (fs.existsSync('public')) {
        const publicFolder = fs.readdirSync('public', { withFileTypes: true })
        for (const publicChild of publicFolder) {
          if (publicChild.isDirectory()) {
            staticAssets.push(`/${publicChild.name}/*`)
          } else {
            staticAssets.push(`/${publicChild.name}`)
          }
        }
      }

      const routes = {
        version: 1,
        include: ['/*'],
        exclude: ['/_astro/*', ...staticPages, ...staticAssets],
      }

      fs.writeFileSync(
        new URL('_routes.json', result.dir),
        JSON.stringify(routes),
      )
    },
  },
}

// https://astro.build/config
export default defineConfig({
  adapter: cloudflare({
    // we handle this with compress()
    imageService: 'passthrough',
    prerenderEnvironment: 'node',
  }),

  i18n: {
    locales: ['en', 'ja'],
    defaultLocale: 'en',
    routing: 'manual',
  },

  site: 'https://aly.fish',

  compressHTML: false,
  vite: {
    build: {
      cssMinify: 'lightningcss',
    },
    css: {
      lightningcss: lightningCssOptions,
    },
  },
  integrations: [
    htmlMinifierNext({
      alwaysWriteMinifiedHTML: true,
      caseSensitive: true,
      collapseBooleanAttributes: true,
      collapseInlineTagWhitespace: false,
      collapseWhitespace: true,
      conservativeCollapse: false,
      continueOnParseError: false,
      decodeEntities: true,
      includeAutoGeneratedTags: true,
      keepClosingSlash: false,
      minifyCSS: lightningCssOptions,
      minifyJS: true,
      minifyURLs: false,
      noNewlinesBeforeTagClose: false,
      preserveLineBreaks: false,
      preventAttributesEscaping: false,
      processConditionalComments: false,
      removeAttributeQuotes: false,
      removeComments: true,
      removeEmptyAttributes: true,
      removeEmptyElements: false,
      removeOptionalTags: false,
      removeRedundantAttributes: true,
      removeScriptTypeAttributes: true,
      removeStyleLinkTypeAttributes: true,
      removeTagWhitespace: false,
      sortAttributes: false,
      sortClassNames: false,
      useShortDoctype: true,
    }),
    compress({
      HTML: false,
      CSS: {
        csso: false,
        lightningcss: lightningCssOptions,
      },
      Exclude: ['88x31.svg'],
    }),
    generateCloudflareRoutes,
  ],
})
